void HornControl_Init(void) {
    IO_SET_HORN_EN(LOW);
}

void HornControl_Run_10ms(void) {

    static uint16_t counter = 0;
    if (IO_GET_HORN_SWITCH_IN() && IO_GET_SW_EN()) {
        if (counter < HORN_MAX_TIME) {
            IO_SET_HORN_EN(HIGH);
            counter++;
        } else {
            IO_SET_HORN_EN(LOW);
        }
    } else {
        IO_SET_HORN_EN(LOW);
        counter = 0;
    }

}

void HornControl_Halt(void) {
    IO_SET_HORN_EN(LOW);
}

